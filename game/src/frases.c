static u16 _next;
static u16 _grupo;
static u8  _string [ FRASES_MAX_CHARS + 1 ];



static void _prepare_string ( u8 *str )
{
	u16 i;

	strclr(_string); // _string [ 0 ] = '\0';

	for ( i = 0; str [ i ] != '\0'; i++ )
	{
		_string [ i ] = str [ i ];

		switch ( _string [ i ] )
		{
			case 250: _string [ i ] = 32 -  1;  break; // ú
			case 243: _string [ i ] = 32 -  2;  break; // ó
			case 241: _string [ i ] = 32 -  3;  break; // ñ
			case 237: _string [ i ] = 32 -  4;  break; // í
			case 233: _string [ i ] = 32 -  5;  break; // é
			case 225: _string [ i ] = 32 -  6;  break; // á
			case 218: _string [ i ] = 32 -  7;  break; // Ú
			case 211: _string [ i ] = 32 -  8;  break; // Ó
			case 209: _string [ i ] = 32 -  9;  break; // Ñ
			case 205: _string [ i ] = 32 - 10;  break; // Í
			case 201: _string [ i ] = 32 - 11;  break; // É
			case 193: _string [ i ] = 32 - 12;  break; // Á
			case 191: _string [ i ] = 32 - 13;  break; // ¿
			case 161: _string [ i ] = 32 - 14;  break; // ¡
		}
	}

	_string [ i ] = '\0';
}




