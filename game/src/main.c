void monos()
{
   JoyReader_update();

   if (!joy1_pressed_btn)
   {
      return;
   }

   SYS_disableInts();
   VDP_loadTileSet(ob_title_monos_mi.tileset, 16, 0);
   PAL_setPalette(PAL1, ob_title_monos_mi.palette->data, CPU);
   SYS_enableInts();

   u16 x = 0;
   u16 y = 0;

   while (1)
   {
      SYS_disableInts();
      VDP_setMapEx(BG_A, ob_title_monos_mi.tilemap, TILE_ATTR_FULL(PAL1, 0, 0, 0, 16), 14, 10, x * 13, y * 6, 13, 6);
      SYS_enableInts();

      ++x;

      if (x == 10)
      {
         x = 0;
         ++y;
      }

      if (x == 6 && y == 6)
      {
         x = 0;
         y = 0;
      }

      JoyReader_update();

      if (joy1_pressed_btn)
      {
         break;
      }

      SYS_doVBlankProcess();
      SYS_doVBlankProcess();
      SYS_doVBlankProcess();
      SYS_doVBlankProcess();
      SYS_doVBlankProcess();
      SYS_doVBlankProcess();
   }

   displayOff(30);
}

